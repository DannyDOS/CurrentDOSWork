    var stickyElement = function stickyElement(el, distance, upOnly) {
        var con = document.querySelector(el);
        var scrollOld = 0;

        var stickEl = function stickEl() {
            var scrollNew = window.pageYOffset;

            if (scrollNew > distance) {
                if (upOnly) {
                    if (scrollNew <= scrollOld) {
                        con.classList.add("back-to-top--active");
                    } else {
                        con.classList.remove("back-to-top--active");
                    }
                } else {
                    con.classList.add("back-to-top--active");
                }
            } else {
                con.classList.remove("back-to-top--active");
            }

            scrollOld = scrollNew;
        };

        window.addEventListener("scroll", stickEl);
    };

