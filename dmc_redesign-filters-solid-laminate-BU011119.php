<h2 class="page-heading h2">Ultra-Thin Worktops</h2>
<p>The surfaces in our collection of ultra-thin worktops do not require specialised equipment to install and can largely be cut and fitted in the same way as laminate worktops - saving costs on installation when compared to the solid stone surfaces they
    imitate.
</p>
<p>Comprised of several layers of laminate, these worktops are more durable than traditional laminate worktops which makes them perfect for busy households. Their solid, non-porous construction ensures they are resistant to mould, mildew, bacteria growth
    and stains. Solid laminate worktops can be cleaned easily with a soft cloth and warm soapy water, making them incredibly low maintenance.</p>
